public class Sum_Of_X_Ranging_From_One_To_Twenty {
    public static void main(String[] args) {
        int sum = 0;
        for (int x = 1; x <= 20; x++)//Calculate the sum of 1 to 20
            sum += x;
        {
            System.out.println("The sum of numbers ranging from 1 to 20 is: " + sum);
            // Print the sum of the series
        }

    }
}